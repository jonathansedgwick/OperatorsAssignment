fun main() {
    var var1 = 10
    var var2 = 5
    println("$var1 + $var2 = " + (var1 + var2))
    println("$var1 - $var2 = " + (var1 - var2))
    println("$var1 * $var2 = " + (var1 * var2))
    println("$var1 / $var2 = " + (var1 / var2))
    println("$var1 % $var2 = " + (var1 % var2))
    println("$var1 equals $var2 = " + (var1 == var2))
}